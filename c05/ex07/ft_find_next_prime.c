int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb <= 2)
		return (2);
	if (nb % 2 == 0)
		nb++;
	i = 3;
	while ((i * i) <= nb)
	{
		if (nb % i == 0)
		{
			nb += 2;
			if (nb % 5 == 0)
				nb += 2;
			i = 1;
		}
		i += 2;
		if (i % 5 == 0)
			i += 2;
	}
	return (nb);
}
